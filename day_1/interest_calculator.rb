puts "This is a simple interest calculator."
puts "It shows the final value of the investment."
# formula for simple interest
puts "The formula is I=P(1 +rt)"
# explain the formula variables
puts "The I is the interest earned."
puts "The P is the initial principal."
puts "The r is the interest rate, in percentage."
puts "The t is the time that the interest is calculated, in years."
# propose some values for the variables
puts "Let's assume that $100 is invested at 5% for 6 months."
# show the values in the formula
puts "The formula becomes I = 100*(1 + (5)*(0.5))."
# calculate the result and print it out
puts "The final value is #{100*(1+(5*0.5))}"
